public class CWH_CH02_Operators {
    public static void main(String[] args) {
        System.out.println(2 == 3);
        System.out.println(2 != 3);
        System.out.println(2 & 3);
        System.out.println(2 == 3 && 2 !=3);
        System.out.println(2 ^ 3);
        System.out.println(2 | 3);
        System.out.println();
    }
}
